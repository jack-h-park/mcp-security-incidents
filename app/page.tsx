import { supabaseAdmin } from '@/lib/supabase-admin'

export const revalidate = 300 // ISR: 5ë¶„

export default async function Page() {
  const { data } = await supabaseAdmin
    .from('incidents')
    .select('id, canonical_key, title, kev, cvss_base, updated_at')
    .order('updated_at', { ascending: false })
    .limit(50)

  return (
    <main style={{padding:24, maxWidth:900, margin:'0 auto'}}>
      <h1>Security Incidents</h1>
      <ul style={{display:'grid', gap:12, marginTop:16}}>
        {(data ?? []).map(x => (
          <li key={x.id} style={{border:'1px solid #eee', borderRadius:8, padding:12}}>
            <a href={`./incidents/${x.id}`} style={{fontFamily:'monospace'}}>
              {x.canonical_key}
            </a>
            {x.kev && <span style={{marginLeft:8}}>ðŸ”¥ KEV</span>}
            {x.cvss_base && <span style={{marginLeft:8}}>CVSS {x.cvss_base}</span>}
            <div style={{fontWeight:600, marginTop:4}}>{x.title}</div>
            <small style={{color:'#666'}}>Updated {new Date(x.updated_at).toLocaleString()}</small>
          </li>
        ))}
      </ul>
    </main>
  )
}


